static int	char_is_upper(char c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	return (0);
}

int	ft_tolower(int c)
{
	if (char_is_upper(c))
		return (c - 'A' + 'a');
	return (c);
}
